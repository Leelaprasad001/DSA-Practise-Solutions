class Solution {
  public:
    int pairsum(vector<int> &arr) {
        // code here
        int max = *max_element(arr.begin(), arr.end());
        for(int i = 0; i < arr.size() ; i++){
            if(arr[i] == max){
                arr.erase(arr.begin() + i);
            }
        }
        int second_max = *max_element(arr.begin(), arr.end());
        return max + second_max;
    }
};


